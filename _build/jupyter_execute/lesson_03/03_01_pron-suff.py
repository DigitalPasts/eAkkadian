# In lesson 1 we have learnt the [independent personal pronouns](/lesson_01/01_06_pronouns_01.ipynb) in nominative case. In this lesson we want to learn the pronominal suffixes. They are declined in:
# 
# - Grammatical Case: **genitive**, **dative**, **accusative**.
# - Grammatical Gender: **masculine** and **feminine**. 
# - Grammatical Person: **1st**, **2nd** and **3rd** person.
# - Grammatical number: **singular** and **plural**.
# 
# (pron-suff_gen)=
# ## Ponominal Suffixes in Genitive
# |               | Gen.      | Translation  | 
# |--------       | ------    |--------      |  
# | 1 sing. com.  | *-ī, -ja, -ʾa* | my
# | 2 sing. masc. | *-ka*     | your            
# | 2 sing. fem.  | *-ki*     | your            
# | 3 sing. masc. | *-šu*     | his             
# | 3 sing. fem.  | *-ša*     | her            
# | 1 plu. com.   | *-ni*      | our
# | 2 plu. masc.  | *-kunu*   | your 
# | 2 plu. fem.   | *-kina*   | your
# | 3 plu. masc.  | *-šunu*   | their 
# | 3 plu. fem.   | *-šina*   | their 
# 
# There are three different forms for the fist person singular. They are used in specific context.
# 
# - The suffix ***-i*** is used when the final phonem of the noun in *status constructus* is a consonant:
#   
#   Example: *ilī* = my god.
#   ```{warning} 
#   :class: dropdown
#   ![It's a trap!](/images/itsatrap.jpg)
# 
#   
#   The form *alī* is identical to the plural in *status rectus*
#   - ***alī*** = my god (nom. or acc.)
#   - ***alī*** = gods (gen.-acc.)
#   ```
# 
# - the suffix ***-ja*** is used when te final phonen of the noun in *status constructus* is a vowel:
#   
#   Example: *īnāya* = my eyes.
# 
#   ```{admonition} Good-to-know
#   :class: tip
#   The suffix ***-ja*** is normally written with the sign <font size="11pt">𒅀</font> 0 ia.
# 
#   For example:
# 
#   <font size="11pt">𒀭𒅀</font> = AN-*ia* = *ilīja* = my gods
#   ```
# 
# - The suffix ***-ʾa*** is used in the Codex Hammurapi. 
#   
#   Example: *awâtūʾa* = my words
# 
#   ```{admonition} Good-to-know
#   :class: tip
#   The suffix ***-ʾa*** is normally written with the sign <font size="11pt">𒀀</font>.
# 
#   For example:
# 
#   <font size="11pt">𒀀𒉿𒌅𒌑𒀀</font> = *a-wa-tu-u₂-a* = *awâtūʾa* = my words
#   <font size="11pt">𒌑𒍪𒊏𒌅𒌑𒀀</font> = *u-ṣu-ra-tu-u₂-a* = *uṣurātūʾa* = my words
#   ```
# 

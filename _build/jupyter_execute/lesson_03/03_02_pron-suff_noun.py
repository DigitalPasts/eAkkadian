# |                 |                      | *šarrassina* | *šarrassina* | *šarratišina*
# 
# ```{note}
# When the final consonant of the root is a dental (*d, t, ṭ*) or a sibilant (*s, ṣ, š, z*), it changes to ***ss*** before the *š* of the 3rd person. For example:
# 
# *šarrassu* = his queen.
# ```

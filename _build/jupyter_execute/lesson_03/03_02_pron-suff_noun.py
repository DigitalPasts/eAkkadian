# # L3.2: Pronominal Suffixes on Noun (*Status Constructus* Part 2)
# In [L1.4](/lesson_01/01_04_status-constructus.ipynb) we learnd how to bind two substatives. In such construction the first noun has to be in ***status consrtuctus***. It has many forms depending on the root, gender and number and even case, but the *status constructus* is also used with [pronominal suffixes](pron-suff_gen) to show possession.
# 
# ```{admonition} Example
# *bēlkīna* = your (fem. plu.) lord.
# *mārūki wardīya imhaṣū* = your (fem.sing.) sons hit my slaves.
# ```
# 
# As you can see from the example above the nound is status constructus and the suffix in genitive:
# 
# ```{admonition} Structure
# *bēl*              + *kīna* 
# Status constructus + Pron. Siff in Genitive
# ```
# The form of the *status constructus* can vary according to the case
# 
# (st-ct_masc)=
# ## Masculine Noun
# More variance is found in the singular forms, the plural is more regular.
# 
# ### Singular
# | *Status rectus* | *Status constructus* | Nominative   | Accusative   | Genitive
# | --------------- | --------             | --------     | -----        | ----- 
# | *bēlum*         | *bēl*                | *bēlī*       | *bēlī*       | *bēlija*
# |                 |                      | *bēl(a)ka*   | *bēl(a)ka*   | *bēlika*
# |                 |                      | *bēl(a)ki*   | *bēl(a)ki*   | *bēliki*
# |                 |                      | *bēl(a)šu*   | *bēl(a)šu*   | *bēlišu*          
# |                 |                     | *bēl(a)ši*   | *bēl(a)ši*   | *bēliši*
# |                 |                    | *bēl(a)ni*   | *bēl(a)ni*   | *bēlini*
# |                 |                   | *bēl(a)kunu* | *bēl(a)kunu* | *bēlikunu*
# |                 |                  | *bēl(a)kina* | *bēl(a)kina* | *bēlikina*
# |                 |                 | *bēl(a)šunu* | *bēl(a)šunu* | *bēlišunu*
# |                 |                | *bēl(a)šina* | *bēl(a)šina* | *bēlišina*
# 
# ```{important}
# the nouns *abum* (father), *ahum* (brother), *emum* (father-in-law) have different forms for each case:
# 
# | *Status rectus* | *Status constructus* | Nominative   | Accusative   | Genitive
# | --------------- | --------             | --------     | -----        | ----- 
# | *abum*          | *abi*                | *abī*        | *abī*        | *abīja*
# | *ahum*          |                      | *abūka*   | *abāka*   | *abīka*
# | *emum*          |                      | *abūki*   | *abāki*   | *abīki*
# |                 |                      | *abūšu*   | *abāšu*   | *abīšu*          
# |                 |                     | *abūši*   | *abāši*   | *abīši*
# |                 |                    | *abūni*   | *abāni*   | *abīni*
# |                 |                   | *abūkunu* | *abākunu* | *abīkunu*
# |                 |                  | *abūkina* | *abākina* | *abīkina*
# |                 |                 | *abūšunu* | *abāšunu* | *abīšunu*
# |                 |                | *abūšina* | *abāšina* | *abīšina*
# ```
# 
# ### Plural
# |  *Status rectus* | *Status constructus* | Nominative  | Gen.-Acc    | 
# | -------------    |---------             | --------    | -----       |  
# |  *šarrū*          | *šarrū*              | *šarrūja*   | *šarrīja*   | 
# |                   |                     | *šarrūka*   | *šarrīka*   | 
# |                   |                     | *šarrūki*   | *šarrīki*   | 
# |                   |                    | *šarrūšu*   | *šarrīšu*   |          
# |                   |                    | *šarrūši*   | *šarrīši*   |
# |                   |                    | *šarrūni*   | *šarrīni*   |
# |                   |                    | *šarrūkunu* | *šarrīkunu* |
# |                   |                     | *šarrūkina* | *šarrīkina* |
# |                   |                     | *šarrūšunu* | *šarrīšunu* |

# # L4.8: III-Weak Verbs
# There are two groups of III-weaks verbs:
# 
# 1. Roots that consist of two consonants and have a long vowel *ī* or *ū*
# 
#     ```{admonition} Example
#     *banûm* "to build" with *ī*
#     *manûm* "to count" with *ū*
#     ```
# 2. Verbs III-*ʾ that can also be divided into two subgroups:
#    - Verbs III-*ʾ* of *ʾ*₁: **without** coloring *a* > *e*.
#    - Verbs III-*ʾ* of *ʾ*₃, *ʾ*₄, *ʾ*₅: **with** coloring *a* > *e*.
# 
# In all Verbs III-weak the originally long vowel shortens in final position:
# 
# ```{admonition} Example
# - With *ī*: *ibni* < \**ibnī* "he built"
# - With *ū*: *imnu* < \**imnū* "hecounted"
# - With *ā*: *ikla* < \**iklā* "he restrained"
# - With *ē*: *ešme* < \**ešmē* "he heard" 
# ```
# 
# The transfer from one group to the other is frequent:
# 
# ```{admonition} Example
# - *imnu* and *imni* "he counted"
# ```
# 
# Verbs III *ī/ē* with following *a* can be contracted or uncontracted:
# 
# ```{admonition} Example
# - *iqbiam* < *iqbi-am* "he said to me".
# - *ibniā* and *ibnâ* "They (fem.) built".
# ```
# 
# Otherwise contracted:
# 
# ```{admonition} Example
# *išemmû* < \**išemmeū* "they hear"
# *ikallû* < \**ikallaū* "they hold back"
# *iqbû* <\**iqbiū* "they said"
# ```
# 
# The *e*-class often changes *a* in neighbouring syllables:
# 
# ```{admonition} Example
# *išemme* < *išamme* "he hears"
# *teleqqe* < \**talaqqe* "you take"
# *iqbû* <\**iqbiū* "they said"
# ```
# 
# ``` {warning}
# With the addition of endings like the personal suffixes, ventive, etc. the original length of the shortened final vowels is restored:
# - *ibnīšu* 
# - *ibnīma*
# - *išmēma*
# - *imnūma*
# ```
# 

# ## I-*ʾ* Verbs
# For these verbs we find the same rules as in the G-stem
# 
# |               | *arākum*   | *epēšum*  | *elûm*    |
# | -------       | ---------- | --------- | --------- |
# | Durative      | *urrak*    | *uppaš*   | *ulla*
# | Perfect       | *ūtarrik*  | *ūteppiš* | *ūtelli*
# | Preterite     | *urrik*    | *uppiš*   | *ulli*
# | Imperative    | *urrik*    | *uppiš*   | *ulli*
# | Participle    | *murrikum* | *muppišum* | *mullûm*
# | Infinitive    | *urrukum*  | *uppušum* | *ullûm*
# | Verbal Adj.   | *urrukum*  | *uppušum* | *ullûm*
# | Stative       | *urruk*    | *uppuš*   | *ullu*  
# 
# 
# ## I-*w*
# For these verbs we find the same rules as in the G-stem. The adjectival as well as the active verbs are conjugated alike. 
# 
# |               | *watārum*  | 
# | -------       | ---------- | 
# | Durative      | *uwattar*    | 
# | Perfect       | *ūtatter*  | 
# | Preterite     | *uwatter*    | 
# | Imperative    | *(w)utter*    | 
# | Participle    | *muwatterum* | 
# | Infinitive    | *(w)utturum*  |
# | Verbal Adj.   | *(w)utturum*  |
# | Stative       | *(w)uttur*    |
# 
# 
# ```{note}
# - In the perfect: *uw-* > *ū*
# - *w* at the beginning is often dropped: *(w)utturum*, *(w)utter*
# ```
# 
# ## II-weak
# These verbs have to be separated into two groups
# 
# ### II-*w* and II-*y*
# They follow the same rules as the G-stem. As for the G-stem, when a vowel follows the *R₃*, this is doubled, that is also true for the infinitve: 
# 
# |               | *kânum*  | 
# | -------       | ---------- | 
# | Durative      | *ukān*, *ukannū* | 
# | Perfect       | *uktīn*, *uktinnū* | 
# | Preterite     | *ukīn*, *ukinnū* | 
# | Imperative    | *kīn*, *kinnā* | 
# | Participle    | *mukinnum* |
# | Infinitive    | *kunnum* |  
# | Verbal Adj.   | *kunnum* |
# | Stative       | *kūn*, *kunnu* | 
# 
# ### II-*ʾ*
# Verbs II-*ʾ* in D-stem are poorly attested. The verbs II₁-₂ (II-*a*) tend to be conjugated with the middle as a strong consonant, although contraction may take place.
# 
# |               | *wârum* strong        | *wârum* weak | 
# | -------       | --------------        | ------------ |
# | Durative      | *uwaʾʾar*, *uwaʾʾarū* | *uwâr*, *uwârrū* | 
# | Perfect       | *ūtaʾʾer*,            |                  | 
# | Preterite     | *uwaʾʾer*, *uwaʾʾerū* | *uwêr*, *uwêrrū* |
# | Imperative    | *wuʾʾer*              |                  | 
# | Participle    | *muwaʾʾerum*          |
# | Infinitive    | *wuʾʾurum*            | *wûrum* 
# | Verbal Adj.   | *wuʾʾurum*            | *wûrum* 
# | Stative       | *wuʾʾur*              | 
# 
# The verbs II₃-₅ (II-*e*) are treated like II-w/y but with *e*-vowels or conjugated like II₁-₂ (II-*a*):
# 
# |               | *rêqum*  | 
# | -------       | ---------- | 
# | Durative      | *urēq*, *ureqqū* | 
# | Perfect       | *urtīq*, *urtiqqū* | 
# | Preterite     | *urīq*, *uriqqu* | 
# | Imperative    | *rīq*, *riqqā* | 
# | Participle    | *muriqqum* |
# | Infinitive    | *ruqqum* |  
# | Verbal Adj.   | *ruqqum* |
# | Stative       | *rūq*, *ruqqu* | 
# 

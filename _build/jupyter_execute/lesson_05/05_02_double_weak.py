# # L5.2: Doubly and Triply Weak Verbs
# There are some verbs with two or even three weak radicals. They have to be learn individually. Here some of the most common ones:
# 
# ## Verbs I-*ʾ* and II-weak
# | Durative       | Perfect  | Preterite    | Participle    | Infinitve | Verbal Adjective |
# | -------------- | -------- | ---------    | ------------  | --------  | ---------------  | 
# | *iʾʾil*        | --       | *īʾil*(?)    | --            | *eʾelum*  | *eʾilum*          |
# 
# *eʾelum* "to bind"
# Andere Verben: 
# - *êrum* "to awaken*
# 
# ## Verbs I-*w* and II-weak
# | Durative       | Perfect  | Preterite    | Participle    | Infinitve | Verbal Adjective |
# | -------------- | -------- | ---------    | ------------  | --------  | ---------------  | 
# | *iwīar*, *i(w)âr* | --    | *iwīr*       | --            | *wiārum*, *wârum*  | *(w)īṣu* |
# 
# *wârum* "to advance"
# Andere Verben: 
# - *wiāṣum* "to become (too) little*
# 
# ## Verbs I-ʾ and III-weak
# | Durative       | Perfect  | Preterite    | Participle    | Infinitve | Verbal Adjective |
# | -------------- | -------- | ---------    | ------------  | --------  | ---------------  | 
# | *illi*         | *itēli*  | *īli*        | *ēlûm*        | *elûm*   | *elûm*            |
# 
# *elûm* "to go up*
# Andere Verben: 
# - *arûm* "to become pregnant*
# 
# ## Verbs I-*w* and III-weak
# | Durative       | Perfect  | Preterite    | Participle    | Infinitve | Verbal Adjective |
# | -------------- | -------- | ---------    | ------------  | --------  | ---------------  | 
# | *uṣṣi*         | *ittaṣi* | *ūṣi*        | *(w)āṣûm*     | *waṣûm*   | *waṣûm*          |
# 
# *waṣûm* "to go out"
# Andere Verben: 
# - *warûm* "to lead*
# - *watûm* "to find, to discover"
# - *wapûm* "to be visible"
# - *waqûm* "to wait*
#   
# ## Verbs II-*ʾ* and III-weak
# | Durative       | Perfect  | Preterite    | Participle    | Infinitve | Verbal Adjective |
# | -------------- | -------- | ---------    | ------------  | --------  | ---------------  | 
# | *ileʾʾi*, *ilê/î* | *ilteʾi*, *iltê* | *ilʾe*, *ilē* | *lēʾûm* | *leʾûm* | *leʾûm*      |
# 
# *leʾûm* "to be able"
# Andere Verben: 
# - *reʾûm* "to pasture, tend*
# - *šeʾûm* "to seek (out)"
# 
# ## Verbs II-*w* and III-weak
# | Durative       | Perfect  | Preterite    | Participle    | Infinitve | Verbal Adjective |
# | -------------- | -------- | ---------    | ------------  | --------  | ---------------  | 
# | *ilawwi*       | *iltawi* | *ilwi*       | *lāwûm*       | *lawûm*   | *lawûm*          |
# 
# *lawûm* "to surround, besiege"
# 
# ## Verbs II-*w* and III-weak
# | Durative       | Perfect  | Preterite    | Participle    | Infinitve | Verbal Adjective |
# | -------------- | -------- | ---------    | ------------  | --------  | ---------------  | 
# | *bâʾum*        | *ibâ*    | *ibtāʾ*      | *ibāʾ*        | *baʾûm*   | --               |
# 
# *bâʾum* "to go along"
# Andere Verben: 
# - *mâʾum* "to be unwilling"
# 
# ## Verbs I, II and III-weak
# | Durative       | Perfect  | Preterite    | Participle    | Infinitve | Verbal Adjective |
# | -------------- | -------- | ---------    | ------------  | --------  | ---------------  | 
# | *iwwi*         | *ītewi*  | *īwi*        | --            | *ewûm*    | --               |
# 
# *ewûm* "to become"
# Andere Verben: 
# - *awûm* "to speak"

# (casus)=
# ## Grammatical Case
# In Akkadian there are three grammatical cases: 
# 
# - The **nominative** indicates the subject in verbal and nominal sentences. It has the following endings:
#   - sg. *-um*
#   - du. *-ān*
#   - m.pl. *-ū* 
#   - f.pl. *-um* 
#   - adj.pl. *-um* 
# 
# 
#   |  Number     | Ending    | Example   | Translation |
#   | ---------   | -------   | -------   | ----------- |
#   | sg.         | *-um*     | *šarr-um*  | king        |
#   | du.         | *-ān*     | *in-ān*    | (both) eyes |
#   | m.pl.       | *-ū*      | *il-ū*     | gods        |
#   | f.pl.       | *-um*     | *šarrāt-um* | queens    |
#   | adj.pl.     | *-um*     | *ṭabūt-um*  | good      |
# 
# - The **genitive** indicates possession. It is used after a prepostition and accompanies the *status constructus*. It has the following endings:
#   - sg. *-im*
#   - du. *-īn*
#   - n.pl. *-ī*; 
#   - n.f.pl. *-im*
#   - adj.pl. *-im*
#   
#   |  Number     | Ending    | Example   | 
#   | ---------   | -------   | -------   | 
#   | sg.         | *-im*     | *šarr-im*  | 
#   | du.         | *-īn*     | *in-īn*    | 
#   | n.pl.       | *-ī*      | *il-ī*     | 
#   | n.f.pl.     | *-im*     | *šarrāt-im* | 
#   | adj.pl.     | *-im*     | *ṭabūt-im*  | 
# 
# - The **accusative:** indicates the direct object of the verbs. It is also used in several adverbial expressions. It has the endings:
#   - sg. *-am*
#   - du. *-īn*
#   - n.pl. *-ī*; 
#   - f.pl. *-im*
#   - adj.pl. *-im*
# 
#   |  Number     | Ending    | Example   | 
#   | ---------   | -------   | -------   | 
#   | sg.         | *-am*     | *šarr-im*  | 
#   | du.         | *-īn*     | *in-īn*    | 
#   | n.pl.       | *-ī*      | *il-ī*     | 
#   | n.f.pl.     | *-im*     | *šarrāt-im* | 
#   | adj.pl.     | *-im*     | *ṭabūt-im*  |
# 
# 
#     ```{warning}
#     The genitive and accusative in dual and plural have the same endings. It is also called ***casus obliquus*** or **gen.-acc.**
#     ```
# 
# ```{note}
# There are two more cases that already in Old Babylonian are not productive anymore: 
# - The **locative** has the ending *-um*. It is replaced by the preposition *ina* + genitiv. It is used mainly in frozen adverial phrases. It can be used with pronominal suffixes. 
# - The **terminative** has the ending *-iš*. It is replaced by the preposition *ana* + genitive.
# 
# |  Case     | Noun    | Femenine |
# | --------- | ------- | ------- |
# | loc.-adv. | *qatum*  | into the hand |  
# | loc.-adv. | *rittūšša* (\<*ritt-um-ša*) | into her hand |
# | ter.-adv. | *ašar-iš* | to the place |
# 
# For more information 👉 GoA 28.2 and 28.3.
# ```
# 
# (gender)=
# ## Grammatical Gender
# There are two grammatical genders: masculine and feminine.
# 
# - **Masculine** 
#   - sg. no special ending.
#   - pl. no special ending. 
# 
# - **Feminine**
#   - sg./du. *-t-* or *-at-*
#   - pl. *-āt-*  
# 
#   ```{admonition} Good-to-Know
#   :class: tip
#   The form of the feminine is actually *-at-* but the short vowel *a* is elided:
#   - *šubtu* < *šub-at-u 
# 
#   In the status constructus the *a* reappears. 👉[L1.3: Status constructus - Part 1](01_03_status-constructus)
#   ```
# 
# | Gender | Number | Case      | Substantive |
# |------- | ------ | -----     | ----------- |
# | m.     | sg.    | nom.      | *šarr-um*   |
# | m.     | pl.    | nom.      | *šarr-ū*   |
# | f.     | sg.    | nom.      | *šarr-at-um*  |
# | f.     | pl.    | nom.      | *šarr-āt-um*  |
# 
# ```{note}
# Some formally masculine nouns are gramatically femenine : 
# 
# | Feminine | Translation |
# |--------- | ----------- |
# | *arhum*  | cow         |
# | *šēpum*  | foot        |
# | *ummum*  | mother      |
# | *enzum*  | she-goat    |
# | *nārum*  | river       |
# 
# For more exception to this rule 👉GoA 2.1(e).
# ```
# (numerus)=
# ## Grammatical Number
# There are thre numbers: 
# 
# - **Singular**: no special ending.
# - **dual**: no special ending.
# 
#     ```{note}
#     Already in Old Babylonian the dual is not being used anymore. Its use is restricted to parts of the body that are pairs, numbers and some other nouns.
#     ```
# - **plural**: 
#   - adj.m. *-ūt-* 
#   - n.f. *-āt*.
#   - adj.f.  *-āt*.
#   
#     | Gender | Number | Case      | Substantive |
#     |------- | ------ | -----     | ----------- |
#     | m.     | sg.    | nom.      | *dann-ūt-um*   |
#     | f.     | pl.    | nom.      | *šarr-āt-um*  | 
#     | m.     | sg.    | nom.      | *dann-āt-um*   |
# 
# ## Paradigm of the Masculine Noun and Adjective
# | Number | Case      | Substantive | Adjective     | 
# |------- | -----     | ----------- | ---------     |
# | sg.    | nom.      | *šarr-um*   | *dann-um*     |  
# |        | gen.      | *šarr-im*   | *dann-im*     |  
# |        | acc.      | *šarr-am*   | *dann-am*     |    
# | dual   | nom.      | *šarr-ān*   | *dann-ūt-um*   |    
# |        | gen./acc. | *šarr-īn*   | *dann-ūt-im*   |        
# | pl.    | nom.      | *šarr-ū*    | *dann-ūt-um*  |   
# |        | gen./acc. | *šarr-ī*    | *dann-ūt-im*  |  
# 
# 
# ## Paradigm of the Feminine Noun and Adjective
# | Number | Case      | Substantive       | Adjective    |
# |------- | -----     | -----------       | ---------    |
# | sg.    | nom.      | *šarr-at-um*      | *dann-at-um* |
# |        | gen.      | *šarr-at-im*      | *dann-at-im* |         
# |        | acc.      | *šarr-at-am*      | *dann-at-am* |
# | dual   | nom.      | *šarr-at-ān*      | *dann-āt-um* |
# |        | gen./acc. | *šarr-at-īn*      | *dann-āt-im* |
# | pl.    | nom.      | *šarr-āt-um*      | *dann-āt-um* |
# |        | gen./acc. | *šarr-āt-im*      | *dann-āt-im* |
# 
# (mim-nun)=
# `````{admonition} Mimmation vs. Nunnation 🧑‍🏫
# :class: tip
# The endings for the singular in the *status rectus* contains the element *-m* and is called [*mimation*](https://en.wikipedia.org/wiki/Mimation). It begins to desappear already in the old Babylonian period, later on completely. For more details see ABG §46; GoA 24.4.There is also the ending *-n* for the dual called [*nunation*](https://en.wikipedia.org/wiki/Nunation). 

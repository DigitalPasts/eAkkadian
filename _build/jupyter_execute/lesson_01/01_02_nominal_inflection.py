# (nom-inflec)=
# # L1.2 Nominal Inflection
# Nouns and adjectves are congruence i.e. the match up in case, gender and number:
# 
# ```{note}
# The adjective is normally placed after the substantive.
# ```
# 
# - [**Grammatical Case**](casus): nominative, genitive, accusative and locative and terminative.
# - [**Grammatical Gender**](gender): masculine and feminine. 
# - [**Grammatical number**](numerus): singular, dual and plural. 
# 
#     ```{note}
#     There was a dual form, but already in Old Babylonian is not productuve anymore.
#     ```

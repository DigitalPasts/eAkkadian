# ## A. Translate and analyse the following words
# 
# ```{admonition} Example
# <font size="11">𒊬𒊒𒌝</font>
#   
# - **Transliteration** : *šar-ru-um*
# - **Normalisation**: *šarrum*
# - **Translation**: king
# - **Status**: *status rectus*
# - **Number**: singular
# - **Case**: nominative
# ```
# 
# <font size="11">𒀭𒄠</font>
# 
# ```{admonition} Solution
# :class: dropdown  
# - **Transliteration** : AN-*am*
# - **Normalisation**: *ilam*
# - **Translation**: god
# - **Status**: *status rectus*
# - **number**: singular
# - **Case**: accusative
# ```
# 
# <font size="11">𒌷𒅎</font>
# 
# ```{admonition} Solution
# :class: dropdown  
# - **Transliteration** : URU-*am*
# - **Normalisation**: *ālim*
# - **Translation**: city
# - **Status**: *status rectus*
# - **number**: singular
# - **Case**: genitive
# ``` 
# 
# <font size="11">𒂗𒌑</font>
# 
# ```{admonition} Solution
# :class: dropdown  

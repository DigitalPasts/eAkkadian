# | 2s.m.   | *tašr**i**q*   | you (ms) stole |
# | 2s.f.   | *tašr**i**qī*  | you (fs) stole |
# | 1s.c.   | *ašr**i**q*    | I stole |
# | 3pl.m.  | *išr**i**qū*   | They (m) stole | 
# | 3pl.f.  | *išr**i**qā*   | They (f) stole |
# | 2pl.c.  | *tašr**i**qā*  | You (pl) stole |
# | 1pl.c.  | *nišr**i**q*   | we stole |

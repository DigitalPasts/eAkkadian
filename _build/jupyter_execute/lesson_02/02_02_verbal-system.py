# ## Non-Finite forms
# Non-finite forms are nominal forms and as such declinable. There are three of them:
# 
# - Infinitive
# - Participle
# - Verbal Adjective
# 
# | Non-Finite Form | Pattern       | Akkadian Word  | Translation    | Hebrew  |

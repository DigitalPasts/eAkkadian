# | Stem Designation  | Form              | Explanation                   | Example        | Translation  | 
# | ----------------  | :---------------: | :---------------------------: | -------------: | ------------ | 
# | *G*               | No changes        | Base meaning. Citation form   | *iprus*        | he separates, he cuts
# | *D*               | Length of *R‚ÇÇ*    | Causative, pluralic           | *uparras*      | He separated 
# | *Gtn*             | *iptarras*      | He blocked continually | Iterative      | 
# | *N*           | *ipparis*       | He is excluded | Passive, middle
# | *Ntn*         | *ipparis*       | He is constantly excluded | Iterative of N-Stem
# | *D*           | *uparras*       | He separated   | Factitive, causative, pluralic, etc. | 
# | *Dt*          | *uptarras*      | It was separated | Passive of D-Stem               |
